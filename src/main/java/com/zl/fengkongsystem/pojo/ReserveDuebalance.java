package com.zl.fengkongsystem.pojo;


public class ReserveDuebalance {

  private String bizdate;
  private String dubillno;
  private Double duebalance;
  private String flag;
  private Double reservebalance;
  private String serialno;


  public String getBizdate() {
    return bizdate;
  }

  public void setBizdate(String bizdate) {
    this.bizdate = bizdate;
  }


  public String getDubillno() {
    return dubillno;
  }

  public void setDubillno(String dubillno) {
    this.dubillno = dubillno;
  }


  public Double getDuebalance() {
    return duebalance;
  }

  public void setDuebalance(Double duebalance) {
    this.duebalance = duebalance;
  }


  public String getFlag() {
    return flag;
  }

  public void setFlag(String flag) {
    this.flag = flag;
  }


  public Double getReservebalance() {
    return reservebalance;
  }

  public void setReservebalance(Double reservebalance) {
    this.reservebalance = reservebalance;
  }


  public String getSerialno() {
    return serialno;
  }

  public void setSerialno(String serialno) {
    this.serialno = serialno;
  }

}
